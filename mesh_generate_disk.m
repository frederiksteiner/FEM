A = [0, -1; 1, 0];
f1 = @(x,y) [(1 + x)*(0.225 - 0.025*y); ...
             (1 + y)*(0.225 - 0.025*x)];
f2 = @(x,y) [(1-x)*(0.225-0.025*y) + 0.5*(1 + x) *cos((1 + y)*pi/8);...
             (1-x)*(0.1 + 0.1*y) + 0.5*(1 + x)*sin((1 +y)*pi/8)];
f3 = @(x,y) [(1-y)*(0.1 + 0.1*x) + 0.5*(1 + y)*sin((1 + x)*pi/8); ...
             (1-y)*(0.225-0.025*x) + 0.5*(1 +y)*cos((1 +x)*pi/8)];
Af1 = @(x,y) A*[(1 + x)*(0.225 - 0.025*y); ...
             (1 + y)*(0.225 - 0.025*x)];
Af2 = @(x,y) A*[(1-x)*(0.225-0.025*y) + 0.5*(1 + x) *cos((1 + y)*pi/8);...
             (1-x)*(0.1 + 0.1*y) + 0.5*(1 + x)*sin((1 +y)*pi/8)];
Af3 = @(x,y) A*[(1-y)*(0.1 + 0.1*x) + 0.5*(1 + y)*sin((1 + x)*pi/8); ...
             (1-y)*(0.225-0.025*x) + 0.5*(1 +y)*cos((1 +x)*pi/8)];
A2f1 = @(x,y) A^2*[(1 + x)*(0.225 - 0.025*y); ...
             (1 + y)*(0.225 - 0.025*x)];
A2f2 = @(x,y) A^2*[(1-x)*(0.225-0.025*y) + 0.5*(1 + x) *cos((1 + y)*pi/8);...
             (1-x)*(0.1 + 0.1*y) + 0.5*(1 + x)*sin((1 +y)*pi/8)];
A2f3 = @(x,y) A^2*[(1-y)*(0.1 + 0.1*x) + 0.5*(1 + y)*sin((1 + x)*pi/8); ...
             (1-y)*(0.225-0.025*x) + 0.5*(1 +y)*cos((1 +x)*pi/8)];
A3f1 = @(x,y) A^3*[(1 + x)*(0.225 - 0.025*y); ...
             (1 + y)*(0.225 - 0.025*x)];
A3f2 = @(x,y) A^3*[(1-x)*(0.225-0.025*y) + 0.5*(1 + x) *cos((1 + y)*pi/8);...
             (1-x)*(0.1 + 0.1*y) + 0.5*(1 + x)*sin((1 +y)*pi/8)];
A3f3 = @(x,y) A^3*[(1-y)*(0.1 + 0.1*x) + 0.5*(1 + y)*sin((1 + x)*pi/8); ...
             (1-y)*(0.225-0.025*x) + 0.5*(1 +y)*cos((1 +x)*pi/8)];
% m.M = {f1, f2, f3};

m.M = {f1, f2, f3, Af1, Af2, Af3, A2f1, A2f2, A2f3, A3f1, A3f2, A3f3};
% J1 = @(x,y) [0.225 - 0.025*y, -0.025*(1+x); ...
%              0.225 - 0.025*x, -0.025*(1+y)];
% J2 = @(x,y) [-(0.225 - 0.025*y) + 0.5*cos((1+y)*pi/8), -0.025*(1 - x) - 1/16*pi*(1 + x)*sin((1 + y)*pi/8);...
%              -(0.1 + 0.1*y) + 0.5*sin((1 + y)*pi/8), 0.1*(1 - x) + pi/16*(1 + x)*cos((1 + y)*pi/8)];
% J3 = @(x,y) [0.1*(1 - y) + pi/16*(1 + y)*cos((1 + x)*pi/8), -(0.1 + 0.1*x) + 0.5*sin((1 + x)*pi/8);...
%              -0.025*(1 - y) - 1/16*pi*(1 + x)*sin((1 + x)*pi/8), -(0.225 - 0.025*x) + 0.5*cos((1+x)*pi/8)];

J1 = @(x,y) [0.225 - 0.025*y, -(1+x)*0.025;
            -(1+y)*0.025,    0.225 - 0.025*x];
        
J2 = @(x,y) [-(0.225 - 0.025*y) + 0.5 * cos((1+y)*pi/8), ...
                -0.025*(1-x) - 0.5*(1+x)*sin((1+y)*pi/8)*pi/8;
            -(0.1 + 0.1*y) + 0.5*sin((1+y)*pi/8), ...
                0.1*(1-x) + 0.5*(1+x)*cos((1+y)*pi/8)*pi/8];
            
J3 = @(x,y) [0.1*(1-y) + 0.5*(1+y)*cos((1+x)*pi/8)*pi/8, ...
                -(0.1+0.1*x) + 0.5*sin((1+x)*pi/8);
            -0.025*(1-y) - 0.5*(1+y)*sin((1+x)*pi/8)*pi/8, ...
                -(0.225 - 0.025*x) + 0.5*cos((1+x)*pi/8)];
AJ1 = @(x,y) A*J1(x,y);
AJ2 = @(x,y) A*J2(x,y);
AJ3 = @(x,y) A*J3(x,y);
A2J1 = @(x,y) A^2*J1(x,y);
A2J2 = @(x,y) A^2*J2(x,y);
A2J3 = @(x,y) A^2*J3(x,y);
A3J1 = @(x,y) A^3*J1(x,y);
A3J2 = @(x,y) A^3*J2(x,y);
A3J3 = @(x,y) A^3*J3(x,y);
m.J = {J1, J2, J3,AJ1,AJ2,AJ3,A2J1,A2J2,A2J3,A3J1,A3J2,A3J3};

